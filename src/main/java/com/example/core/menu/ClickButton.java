package com.example.core.menu;

/**
 * view类型按钮类ViewButton
 * 
 * @author liuxun
 *
 */
public class ClickButton extends BasicButton {
	private String type = Menu.CLICK;
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}
