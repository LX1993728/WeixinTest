package com.example.po.template;
/**
 *  阶段场景特殊项—— 情景概述(首项)
 * @author liuxun
 *
 */

import java.util.List;

public class StageSceneFirst extends StageSceneBase {
	private static final long serialVersionUID = 1L;
	private String desc; // 演练情景描述
	private String rule; // 演练原则说明
	private List<String> targets; // 演练目标集合
	private String explain; // 演练场景说明
	private List<ImageText> imgts; // (媒体-信息) 集合

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getRule() {
		return rule;
	}

	public void setRule(String rule) {
		this.rule = rule;
	}

	public List<String> getTargets() {
		return targets;
	}

	public void setTargets(List<String> targets) {
		this.targets = targets;
	}

	public String getExplain() {
		return explain;
	}

	public void setExplain(String explain) {
		this.explain = explain;
	}

	public List<ImageText> getImgts() {
		return imgts;
	}

	public void setImgts(List<ImageText> imgts) {
		this.imgts = imgts;
	}

}
